using System.Collections.Generic;

namespace GameBarBrowser
{
    public static class ExtensionMethods
    {
        public static void Swap<T>(this IList<T> list, int indexA, int indexB)
        {
            T tmp = list[indexA];
            list[indexA] = list[indexB];
            list[indexB] = tmp;
        }
    }
}
